package com.epam.training.examples;

public class Primitives {

    private static char c;
    private static boolean b;
    private static int i;
    private static long l;
    private static float f;
    private static double d;

    public static void main(String[] args) {
        System.out.println("Default value of a char variable is '" + c + "'");
        System.out.println("Default value of a boolean variable is '" + b + "'");
        System.out.println("Default value of an integer variable is '" + i + "'");
        System.out.println("Default value of a long variable is '" + l + "'");
        System.out.println("Default value of a float variable is '" + f + "'");
        System.out.println("Default value of a double variable is '" + d + "'");
    }

}
